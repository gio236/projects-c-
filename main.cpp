void mvmcolmap (int mappa[20][40]){

		ch = getch();

			if(ch == KEY_DOWN || ch == 's'){
			mvaddch(y, x, ' ');
			y++;
			if (mappa[y][x] == 1){
				y--;
				mvaddch(y, x, '@');				
			}else{
				mvaddch(y, x, '@');
				refresh();
			}
		}else if(ch == KEY_UP || ch == 'w'){
			mvaddch(y, x, ' ');
			y--;
			if (mappa[y][x] == 1){			
				y++;
				mvaddch(y, x, '@');
			}else{
				mvaddch(y, x, '@');
				refresh();
			}
		}else if(ch == KEY_LEFT || ch == 'a'){
			mvaddch(y, x, ' ');
			x--;
			if (mappa[y][x] == 1){			
				x++;
				mvaddch(y, x, '@');
			}else{
				mvaddch(y, x, '@');
				refresh();
			}
		}else if(ch == KEY_RIGHT || ch == 'd'){
			mvaddch(y, x, ' ');
			x++;
			if (mappa [y][x] == 1){
				x--;
				mvaddch(y, x, '@');
			}else{
				mvaddch(y, x, '@');
				refresh();
			}
	  	}
}

void spawn(int mappa[][40]){
for (int i = 0;i < 20; i++){
	for (int n = 0;n < 40; n++){
		if (mappa[i][n] == 1){
			mvaddch(i, n, '#');
		}else if (mappa [i][n] == 3 || mappa [i][n] == 2 || mappa [i][n] == 4 || mappa [i][n] == 5 || mappa [i][n] == 6 || mappa [i][n] == 7 || mappa [i][n] == 8 || mappa [i][n] == 9){
			mvaddch(i, n, '|');
		}else{
			mvaddch(i, n, ' ');
		}
	}
}
}



